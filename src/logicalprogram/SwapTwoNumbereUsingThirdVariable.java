package logicalprogram;

public class SwapTwoNumbereUsingThirdVariable {

    public static void main (String [] args)
    {

        int A=5;
        int B=6;
        int Z;

        System.out.println("Befor swaping:-"+A+" : "+B);
        //swaping
        Z=A;
        A=B;
        B=Z;
        System.out.println("After swaping:-"+A+" : "+B);

    }
}
